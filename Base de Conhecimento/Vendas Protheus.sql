			   ,F2.F2_DOC AS NF 
			   ,F2.F2_SERIE AS SERIE 
			   ,F2.F2_FILIAL AS COD_FILIAL 
			,CASE WHEN F2_FILIAL = '010101' THEN 'BRAS펣IA - MATRIZ' 
				  WHEN F2_FILIAL = '010103' THEN 'BRAS펣IA - CD'
				  WHEN F2_FILIAL = '010104' THEN 'CRISTALINA' 
				  WHEN F2_FILIAL = '010105' THEN 'UNAI'
				  WHEN F2_FILIAL = '010106' THEN 'FORMOSA' 
				  WHEN F2_FILIAL = '010107' THEN 'PARACATU'
				  WHEN F2_FILIAL = '010109' THEN 'PADRE BERNARDO' 
				  WHEN F2_FILIAL = '010110' THEN 'BURITIS' 
				  WHEN F2_FILIAL = '010111' THEN 'PORTO'
				  WHEN F2_FILIAL = '010112' THEN 'LAGOA' 
				  WHEN F2_FILIAL = '010113' THEN 'ARAGUAINA'
				  WHEN F2_FILIAL = '010114' THEN 'PALMAS'
				  WHEN F2_FILIAL = '010115' THEN 'GURUPI'
				  WHEN F2_FILIAL = '010116' THEN 'PARAISO'
				  WHEN F2_FILIAL = '010117' THEN 'GUARAI'
				  WHEN F2_FILIAL = '010118' THEN 'CASEARA'
				  WHEN F2_FILIAL = '010119' THEN 'PADDF'
				  WHEN F2_FILIAL = '010120' THEN 'MARIANOPOLIS' 
			 END AS NOME_FILIAL
				,A1.A1_CGC AS CGCCPF 
				,A1.A1_NOME AS CLIENTE 
				,A1.A1_END AS COD_ENDERECO 
				,A1.A1_MUN AS CIDADE 
				,A1.A1_EST AS UF 
				,D2.D2_TES AS COD_TRANSACAO 
				,F4.F4_TEXTO AS TRANSACAO 
				,D2.D2_CF AS CFOP
				,D2.D2_EMISSAO AS DATA_NF 
				,D2.D2_COD AS COD_PRODUTO 
				,B1.B1_DESC AS DESC_PRODUTO 
				,D2.D2_QUANT AS QUANTIDADE 
				,D2.D2_TOTAL AS VALOR 
				,D2.D2_CUSTO1 AS CMV 
				,(D2.D2_VALICM+D2.D2_VALPIS+D2.D2_VALCOF+D2.D2_VALCSL+D2.D2_VALIMP5+D2.D2_VALIMP6+D2.D2_ICMSRET+D2.D2_VALISS) AS IMPOSTOS 
			,CASE WHEN B1.B1_GRUPO = 'VEIC' THEN 'MAQUINAS'
				  WHEN B1.B1_GRUPO IN ('SRV','MO_G','MO_O','MO_T','GESC','GEOC','GESI') THEN 'SERVI큞S'
				  WHEN B1.B1_GRUPO NOT IN ('VEIC','SRV','MO_G','MO_O','MO_T','GESC','GEOC','GESI') THEN 'PE큐S' 
			 END AS SETOR 
			,CASE WHEN F2.F2_PREFORI IN ('','VEI') THEN 'VEICULOS'
				  WHEN F2.F2_PREFORI = 'BAL' THEN 'BALCAO'
				  WHEN F2.F2_PREFORI = 'OFI' THEN 'OFICINA'
			 END AS COD_SETOR
			,CASE WHEN B1.B1_GRUPO = 'VEIC' THEN 'MAQUINAS'
				  WHEN B1.B1_GRUPO IN ('SRV','MO_G','MO_O','MO_T') THEN 'SERVI큞S'
				  WHEN B1.B1_GRUPO IN ('GESC','GEOC','GESI') THEN 'GARANTIAS'
				  WHEN B1.B1_GRUPO NOT IN ('VEIC','SRV','MO_G','MO_O','MO_T','GESC','GEOC','GESI') THEN 'PE큐S' 
			 END AS TIPO_ITEM
			,A3.A3_COD AS COD_VEND
			,A3.A3_NOME AS NOME_VEND

FROM SF2010 AS F2 WITH (NOLOCK)
INNER JOIN SD2010 AS D2 WITH (NOLOCK)
ON  D2.D2_FILIAL = F2.F2_FILIAL
AND D2.D2_DOC = F2.F2_DOC 
AND D2.D2_SERIE = F2.F2_SERIE
AND D2.D2_CLIENTE = F2.F2_CLIENTE
AND D2.D2_LOJA = F2.F2_LOJA
AND D2.D_E_L_E_T_ = ''
AND F2.D_E_L_E_T_ = ''
LEFT JOIN SA1010 AS A1  WITH (NOLOCK)
ON A1.A1_COD = D2.D2_CLIENTE 
AND A1.A1_LOJA = D2.D2_LOJA 
AND A1.D_E_L_E_T_ = ''
LEFT JOIN SF4010 AS F4  WITH (NOLOCK)
ON F4.F4_CODIGO = D2.D2_TES 
AND F4.D_E_L_E_T_ = ''
LEFT JOIN SB1010 AS B1	WITH (NOLOCK) 
ON B1.B1_COD = D2.D2_COD 
AND B1.D_E_L_E_T_ = ''
LEFT JOIN SA3010 AS A3 WITH (NOLOCK)
ON A3.A3_COD = F2.F2_VEND1 
AND A3.D_E_L_E_T_ = ''
WHERE 
D2.D2_TES IN ('501','502','503','504','505','506','507','508','509','510','511','512','513','514','515','516','517','518','519','520','521','522','523','524','525','526','527','528','529','530','531','532','533','534','535','550','551','552','553','554','650','750','751','752','753','754','755','756','757','758','759','770','771','791','946','947','950','735','736','737','738','507','908','910','911','912')
				,F1.F1_FILIAL AS COD_FILIAL 
			,CASE WHEN F1_FILIAL = '010101' THEN 'BRAS펣IA - MATRIZ' 
				  WHEN F1_FILIAL = '010103' THEN 'BRAS펣IA - CD'
				  WHEN F1_FILIAL = '010104' THEN 'CRISTALINA' 
				  WHEN F1_FILIAL = '010105' THEN 'UNAI'
				  WHEN F1_FILIAL = '010106' THEN 'FORMOSA' 
				  WHEN F1_FILIAL = '010107' THEN 'PARACATU'
				  WHEN F1_FILIAL = '010109' THEN 'PADRE BERNARDO' 
				  WHEN F1_FILIAL = '010110' THEN 'BURITIS' 
				  WHEN F1_FILIAL = '010111' THEN 'PORTO'
				  WHEN F1_FILIAL = '010112' THEN 'LAGOA' 
				  WHEN F1_FILIAL = '010113' THEN 'ARAGUAINA'
				  WHEN F1_FILIAL = '010114' THEN 'PALMAS'
				  WHEN F1_FILIAL = '010115' THEN 'GURUPI'
				  WHEN F1_FILIAL = '010116' THEN 'PARAISO'
				  WHEN F1_FILIAL = '010117' THEN 'GUARAI'
				  WHEN F1_FILIAL = '010118' THEN 'CASEARA'
				  WHEN F1_FILIAL = '010119' THEN 'PADDF'
				  WHEN F1_FILIAL = '010120' THEN 'MARIANOPOLIS' 
			END AS NOME_FILIAL
				,A2.A2_CGC AS CGCCPF 
				,A2.A2_NOME AS CLIENTE 
				,A2.A2_END AS COD_ENDERECO 
				,A2.A2_MUN AS CIDADE 
				,A2.A2_EST AS UF 
				,D1.D1_TES AS COD_TRANSACAO 
				,F4.F4_TEXTO AS TRANSACAO
				,D1.D1_CF AS CFOP
				,D1.D1_EMISSAO AS DATA_NF 
				,D1.D1_COD AS COD_PRODUTO 
				,B1.B1_DESC AS DESC_PRODUTO 
				,D1.D1_QUANT AS QUANTIDADE 
				,D1.D1_TOTAL * -1 AS VALOR 
				,D1.D1_CUSTO * -1 AS CMV 
				,(D1.D1_VALICM+D1.D1_VALPIS+D1.D1_VALCOF+D1.D1_VALCSL+D1.D1_VALIMP5+D1.D1_VALIMP6+D1.D1_ICMSRET+D1.D1_VALISS) * -1 AS IMPOSTOS 
			,CASE WHEN B1.B1_GRUPO = 'VEIC' THEN 'MAQUINAS'
				  WHEN B1.B1_GRUPO IN ('SRV','MO_G','MO_O','MO_T','GESC','GEOC','GESI') THEN 'SERVI큞S'
				  WHEN B1.B1_GRUPO NOT IN ('VEIC','SRV','MO_G','MO_O','MO_T','GESC','GEOC','GESI') THEN 'PE큐S'
			 END AS SETOR 
				,'' AS COD_SETOR
			,CASE WHEN B1.B1_GRUPO = 'VEIC' THEN 'MAQUINAS'
				  WHEN B1.B1_GRUPO IN ('SRV','MO_G','MO_O','MO_T') THEN 'SERVI큞S'
				  WHEN B1.B1_GRUPO IN ('GESC','GEOC','GESI') THEN 'GARANTIAS'
				  WHEN B1.B1_GRUPO NOT IN ('VEIC','SRV','MO_G','MO_O','MO_T','GESC','GEOC','GESI') THEN 'PE큐S' 
			 END AS TIPO_ITEM 
			,'ENTRADA' AS COD_VEND
			,'ENTRADA' AS NOME_VEND
FROM SF1010  AS F1 WITH (NOLOCK)
INNER JOIN SD1010 AS D1 WITH (NOLOCK)
ON  D1.D1_FILIAL = F1.F1_FILIAL
AND D1.D1_DOC = F1.F1_DOC 
AND D1.D1_SERIE = F1.F1_SERIE
AND D1.D1_FORNECE = F1.F1_FORNECE
AND D1.D1_LOJA = F1.F1_LOJA
AND D1.D_E_L_E_T_ = ''
AND F1.D_E_L_E_T_ = ''
LEFT JOIN SA2010 AS A2  WITH (NOLOCK)
ON A2.A2_COD = D1.D1_FORNECE 
AND A2.A2_LOJA = D1.D1_LOJA 
AND A2.D_E_L_E_T_ = ''
LEFT JOIN SF4010 AS F4  WITH (NOLOCK)
ON F4.F4_CODIGO = D1.D1_TES 
AND F4.D_E_L_E_T_ = ''
LEFT JOIN SB1010 AS B1	 WITH (NOLOCK)
ON B1.B1_COD = D1.D1_COD 
AND B1.D_E_L_E_T_ = ''
WHERE F1.F1_TIPO = 'D' 
AND F4.F4_CODIGO IN ('200','201','202','203','204','205','206','207','215','216','217','218','219','220','221','222','223','224','225','226','227','228','229','230','231','232')
	
ORDER BY COD_FILIAL,NF
